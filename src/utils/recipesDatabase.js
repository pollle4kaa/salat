export function findRecipesByIngredients(userIngredients) {
  return recipesDatabase.map(recipe => {
    const matchingIngredients = recipe.ingredients.filter(ing =>
      userIngredients.some(userIng =>
        userIng.toLowerCase().includes(ing.toLowerCase()) ||
        ing.toLowerCase().includes(userIng.toLowerCase())
      )
    );
    const matchScore = matchingIngredients.length / recipe.ingredients.length;
    return {
      ...recipe,
      matchScore,
      missingIngredients: recipe.ingredients.filter(ing =>
        !userIngredients.some(userIng =>
          userIng.toLowerCase().includes(ing.toLowerCase())
        )
      )
    };
  }).filter(recipe => recipe.matchScore > 0)
  .sort((a, b) => b.matchScore - a.matchScore);
}

export function scaleRecipe(recipe, desiredPortions) {
  const scale = desiredPortions / recipe.basePortions;
  return {
    ...recipe,
    scaledPortions: desiredPortions,
    caloriesPerPortion: Math.round(recipe.caloriesPerPortion * scale),
    proteinPerPortion: Math.round(recipe.proteinPerPortion * scale),
    fatPerPortion: Math.round(recipe.fatPerPortion * scale),
    carbsPerPortion: Math.round(recipe.carbsPerPortion * scale)
  };
}